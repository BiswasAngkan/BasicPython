# To perform diffrent kinds of operations on a variable 

'''Membership Operator'''
x = [1, 2, 3, 4, 5, 6]
print(x)
print(2 in x)
print(7 in x)

'''Logical/Relational Operator'''
x = True
print(not x)
x = [1, 2, 3, 4, 5, 6]
print(x)
print(2 not in x)
print(True or True)
print(True and False)

'''Comparison Operator'''
x = [1, 2, 3, 4, 5, 6]
print([a == 2 for a in x])
print([a<2 for a in x])
print([a>2 for a in x])
print([a!=2 for a in x])
print([a>=2 for a in x])
print([a<=2 for a in x])
