# To perform assignment & aritmetic operation

'''Assignment Operator'''
x = 2
print(x)
x += 2					# x = x + 2
print(x)
y = 2
y -= 2					# y = y - 2
print(y)
x *= 2 					# x = x * 2
print(x)
x /= 2					# x = x / 2
print(x)

'''Aritmetic Operator'''
x = 3 + 2; print(x)
x = 3 - 2; print(x)
x = 3 * 2; print(x)
x = 3 / 2; print(x)
x = 3 // 2; print(x)
x = 3 ++ 2; print(x)
x = 3 ** 2; print(x)
x = 3 % 2; print(x)
